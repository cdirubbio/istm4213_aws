    <h2>Department of Information Systems & Technology Management</h2>
    <p>The MS in Information Systems Technology program provides a high-quality technical education directed towards those who develop information systems in the private or public sectors and those who require specialized management education designed to enhance skills in the management of information systems and development projects.</p>

    <h3>Our Focus</h3>
    <p>Our focus provides the appropriate blend of hands-on exposure to technology, problem-solving skills, and conceptual frameworks for understanding technology so that students can be effective in business or government settings using current information technologies as well as new and emerging technologies.</p>

    <h3>Our Programs</h3>
    <p>The Master of Science in Information Systems Technology (MSIST) program is designed for individuals who are (or will become) managers in organizations active in scientific or technical environments. The MSIST program has dual objectives:</p>
    <ul>
        <li>Promoting more effective management by technologists and scientists in organizations.</li>
        <li>Increasing the non-technical manager’s understanding of the role of science and technology in the successful growth and operation of organizations.</li>
    </ul>

    <p>The MSIST program promotes study of the innovation environment — problems in setting strategy for the development of technical products, processes, and services, managing creative professionals, introduction and adoption of new technology, and mastery of the resultant organizational change.</p>

    <h3>Ph.D. Program</h3>
    <p>The Ph.D. in Business with an Information Systems and Technology Management (ISTM) Area of Focus focuses on the research areas in information systems and technology management, preparing students for successful research careers in the IS fields.</p>

